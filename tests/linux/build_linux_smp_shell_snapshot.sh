export AARCHVM_USERTEST_SNAPSHOT_VERIFY_LOG="${AARCHVM_USERTEST_SNAPSHOT_VERIFY_LOG:-out/linux-smp-shell-v1-verify.log}"

exec ./tests/linux/build_linux_shell_snapshot.sh
